package com.ikang.model;

public class Message {
    private int id;
    private String name;


    public Message(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Message(int id) {
        this.id = id;
    }

    public int Message(int a, int b, int c) {
        return a + b + c;
    }

}
